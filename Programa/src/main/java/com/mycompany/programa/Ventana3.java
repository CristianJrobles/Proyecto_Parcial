    private void AceptapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptapActionPerformed
        // TODO add your handling code here:
        ArchivoPlano plano= new ArchivoPlano();
        Informacion inf=new Informacion();
        
        if(Local.getSelectedItem()==Visitante.getSelectedItem()){
            JOptionPane.showMessageDialog(null,"Selecciona Equipos Diferentes");
        }
        else{
            inf.setDia((String) varDia.getSelectedItem());
            inf.setMes((String) varMes.getSelectedItem());
            inf.setAño((String) varAño.getSelectedItem());

            inf.setNombre((String) Local.getSelectedItem());
            inf.setNombre2((String) Visitante.getSelectedItem());
            inf.setGoles((String) GolLocal.getSelectedItem());
            inf.setGoles2((String) GolVisitante.getSelectedItem());

            String fila2= inf.getDia()+"/"+inf.getMes()+"/"+inf.getAño()+";"+inf.getNombre()+";"
                    +inf.getGoles()+";"+inf.getGoles2()+";"+inf.getNombre2()+"\n";

            plano.guardarPartido(fila2);
            JOptionPane.showMessageDialog(null,"Guardado Correctamente");
        }
       
    }//GEN-LAST:event_AceptapActionPerformed

