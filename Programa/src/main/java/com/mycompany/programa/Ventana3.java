    public void comboBox(){
        ArchivoPlano archi = new ArchivoPlano();
        ArrayList<Object[]>datos=archi.leer();
        
        for (Object[] dat : datos){
            Local.addItem((String) dat[1]);
            Visitante.addItem((String) dat[1]);
        }
    }
